#[derive(Debug)]
pub enum TokenKind {
    LeftParen,
    RightParen,
    LeftBrace,
    RightBrace,
    Comma,
    Dot,
    Minus,
    Plus,
    Semicolon,
    Slash,
    Star,
    Bang,
    BangEqual,
    Equal,
    EqualEqual,
    Greater,
    GreaterEqual,
    Less,
    LessEqual,
    Identifier,
    String,
    Number,
    And,
    Struct,
    Else,
    False,
    For,
    Fn,
    If,
    None,
    Or,
    Print,
    Return,
    Super,
    Self_,
    True,
    Let,
    While,
    Error,
    EOF,
}

pub struct Token {
    pub kind: TokenKind,
    pub lexeme: String,
    pub line_number: usize,
}
